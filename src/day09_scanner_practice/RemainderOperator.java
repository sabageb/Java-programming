package day09_scanner_practice;

import java.sql.SQLOutput;

public class RemainderOperator {
    public static void main(String [] args) {

        System.out.println(10/3);//3
        System.out.println(10 % 3);//1 /3+3+3=9 then 1 remainder
        System.out.println(10/5);//2
        System.out.println(10%5); //0  5+5+0= 10
        System.out.println(20 / 7 );// 2
        //7+7=14 6 is remainder 14+6=20
        System.out.println(20 % 7); // 6 is remainder
        System.out.println(100 % 60);

    }
}
