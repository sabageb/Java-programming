package day07_arithmetic_operators_casting;

public class StringConcatPractice {
    public static void main(String[] args) {

        String str1 = " Hello";
        String str2 = " friends";
        System.out.println(str1 + str2);
        // hello friends

        System.out.println(str1 +" " +str2);

        int num1= 7;
        int num2= 8;

        System.out.println(num1+num2);//15
        // 7 8

        System.out.println(num1 +" "+num2);
    }
}
